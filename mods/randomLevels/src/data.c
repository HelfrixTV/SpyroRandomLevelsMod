void randomiseLevels(int seed) {
	
	for(int i = 0; i < 29; i++){
		newPortalLevels[i] = currentPortalLevels[i];
	}
	int d = 0;
	srand(seed);
	while(d < 10){ //make the random more juicy
		//29 levels
		for(int i = 0; i < 29; i++){    // shuffle array
			int temp = newPortalLevels[i];
			int randomIndex = (int)rand(seed) % 29;

			newPortalLevels[i] = newPortalLevels[randomIndex];
			newPortalLevels[randomIndex] = temp;
		}
		d++;
	}
	
}